package com.example.zoo_application.VA;

public class CharacterParts {
    private int X;

    private int Y;

    private int Width;

    private int Height;

    public CharacterParts(int x, int y, int width, int height) {
        X = x;
        Y = y;
        Width = width;
        Height = height;
    }

    public int getX() {
        return X;
    }

    public int getY() {
        return Y;
    }

    public int getWidth() {
        return Width;
    }

    public int getHeight() {
        return Height;
    }
}
